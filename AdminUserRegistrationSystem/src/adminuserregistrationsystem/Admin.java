    private void genreportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genreportActionPerformed
        // TODO add your handling code here:
        try {
        // Assuming you want to generate a report of all registered users
        // You can customize this query according to your database schema
        Class.forName("com.mysql.jdbc.Driver");
        sqlConn = DriverManager.getConnection(dataConn, username, password);
        pst = sqlConn.prepareStatement("select * from registrationsystem");
        rs = pst.executeQuery();
        
        // Display the report in a new window or dialog
        // You can use a JTextArea, JTable, or any other component to display the report
        StringBuilder reportBuilder = new StringBuilder();
        while (rs.next()) {
            // Append user details to the report string
            reportBuilder.append("Username: ").append(rs.getString("username")).append(", ");
            reportBuilder.append("Password: ").append(rs.getString("password")).append(", ");
            reportBuilder.append("User Type: ").append(rs.getString("usertype")).append("\n");
        }
        
        // Create a JTextArea to display the report
        JTextArea reportTextArea = new JTextArea(reportBuilder.toString());
        reportTextArea.setEditable(false); // Make it read-only
        
        // Show the report in a JOptionPane
        JOptionPane.showMessageDialog(this, new JScrollPane(reportTextArea), "User Report", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error generating report: " + ex.getMessage());
    } finally {
        try {
            // Close resources in finally block
            if (rs != null) {
                rs.close();
            }
            if (pst != null) {
                pst.close();
            }
            if (sqlConn != null) {
                sqlConn .close();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_genreportActionPerformed

