    private void baddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baddActionPerformed
         try {
        // Validate input fields
        String username = jTextField1.getText().trim();
        String password = jTextField2.getText().trim();
        String userType = jTextField3.getText().trim();
        
        if (username.isEmpty() || password.isEmpty() || userType.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return; // Exit the method if any field is empty
        }

        // Check if the username already exists in the database
        if (usernameExists(username)) {
            JOptionPane.showMessageDialog(this, "Username already exists. Please choose a different one.");
            return; // Exit the method if the username already exists
        }

        Class.forName("com.mysql.jdbc.Driver");
        sqlConn = DriverManager.getConnection(dataConn, username, password);
        pst = sqlConn.prepareStatement("INSERT INTO registrationsystem (username, password, usertype) VALUES (?, ?, ?)");

        pst.setString(1, username);
        pst.setString(2, password);
        pst.setString(3, userType);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "User Added!");
        displayUserData();
    } catch (ClassNotFoundException ex) {
        System.out.println("MySQL JDBC Driver not found.");
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_baddActionPerformed
private boolean usernameExists(String username) throws SQLException {
    // Check if the username exists in the database
    pst = sqlConn.prepareStatement("SELECT * FROM registrationsystem WHERE username = ?");
    pst.setString(1, username);
    ResultSet rs = pst.executeQuery();
    return rs.next(); // Return true if username exists, false otherwise
}
    private void bdelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdelActionPerformed
       try {
        // Get the selected row index
        int rowIndex = jTable1.getSelectedRow();
        
        // Get the username from the selected row
        String usernameToDelete = jTable1.getValueAt(rowIndex, 0).toString();
        
        // Delete the user from the database
        Class.forName("com.mysql.jdbc.Driver");
        sqlConn = DriverManager.getConnection(dataConn, username, password);
        pst = sqlConn.prepareStatement("DELETE FROM registrationsystem WHERE username=?");
        pst.setString(1, usernameToDelete);
        pst.executeUpdate();
        
        // Display a success message
        JOptionPane.showMessageDialog(this, "User deleted successfully!");
        
        // Update the table with the latest data
        displayUserData();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
    }//GEN-LAST:event_bdelActionPerformed

